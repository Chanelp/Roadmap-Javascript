/* Operador ternario: (condicion) ? verdadero : falsa */

console.log("Operador ternario");

let edad = 17;
let eresMayor = (edad >= 18) 
    ? "Eres mayor de edad" 
    : "Eres menor de edad";

console.log(eresMayor);

