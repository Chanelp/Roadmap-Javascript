// If else + if else

let nota = 1;

if (nota === 5) {
    console.log("Enhorabuena, has obtenido un sobresaliente");
} else if(nota === 4) {
    console.log("Buen trabajo!");
} else if(nota === 3) {
    console.log("Suficiente")
} else if(nota === 2) {
    console.log("No has aprobado")
} else if(nota === 1) {
    console.log("No has estudiado nada, trabaja mas para la proxima")
} else {
    console.log("Error, introduce una nota entre el 1 y el 5")
}